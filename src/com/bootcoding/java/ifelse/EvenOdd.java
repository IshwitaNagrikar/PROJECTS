package com.bootcoding.java.ifelse;

public class EvenOdd {
    public static void main(String[] args) {
        int num = 57;
        if (num % 2 == 0)
        {
            System.out.println("The given number is Even");
        }
        else {
            System.out.println("The given number is Odd");
        }
    }
}
