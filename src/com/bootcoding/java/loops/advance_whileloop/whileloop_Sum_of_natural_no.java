package com.bootcoding.java.loops.advance_whileloop;

public class whileloop_Sum_of_natural_no {
    public static void main(String[] args) {
        int a=0, sum=0;
        int i=a+1;
        while (i<=10)
        {
            sum=sum+i;
            i++;
        }
        System.out.println("sum of natural number is: "+sum);
    }
}
