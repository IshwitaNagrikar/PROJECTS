package com.bootcoding.java.switch_case_statements;

public class vowel_consonent {
    public static void main(String[] args) {
        char alphabet = 'z';
        switch (alphabet)
        {
            case 'a' :
                System.out.println("It is a vowel");
                break;
            case 'e' :
                System.out.println("It is a vowel");
                break;
            case 'i' :
                System.out.println("It is a vowel");
                break;
            case 'o' :
                System.out.println("It is a vowel");
                break;
            case 'u' :
                System.out.println("It is a vowel");
                break;
            default:
                System.out.println("It is a consonent");

        }
    }
}
